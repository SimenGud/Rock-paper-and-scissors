package org.example.rockpaperscissorsjava;

public class rpsApp

{
  String Rock = "Rock";
  String Paper = "Paper";
  String Scissors = "Scissors";

  public rpsApp()
  {
    this.Rock = Rock;
    this.Paper = Paper;
    this.Scissors = Scissors;
  }

  public void setRock(String Rock)
  {
    this.Rock = Rock;
  }

  public void setPaper(String Paper)
  {
    this.Paper = Paper;
  }

  public void setScissors(String Scissors)
  {
    this.Scissors = Scissors;
  }

}
